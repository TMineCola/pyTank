class Tank_Sprite(pygame.sprite.Sprite):
    def __init__(self,image_file):
        pygame.sprite.Sprite.__init__(self)
        self.image = pygame.image.load("images/" + image_file).convert()
        # 設定透明的 colorkey, 如果圖片的顏色與 colorkey 所指定的顏色相同則會顯示成透明
        # 目前是圖片中黑色的部分會顯示成透明
        self.image.set_colorkey((255, 255, 255))
        # 物件的 rect(長方形) 以圖片的長寬來定義
        self.rect = self.image.get_rect()

class Brick(Tank_Sprite):
    # 由於磚塊初始化位置不同, 所以另外傳入初始化位置
    def __init__(self, image_file, x, y):
    # 設定磚塊位置
        Tank_Sprite.__init__(self, image_file)
        self.rect.x, self.rect.y = x, y

class Tank(Tank_Sprite):
    def __init__(self, image_file):
        Tank_Sprite.__init__(self, image_file)
        self.rect.bottom = WINDOW_HEIGHT
        self.rect.left = (WINDOW_WIDTH - self.image.get_width()) / 2
    def move_left(self):
        # 如果坦克還沒碰到左邊邊界
        self.image = pygame.image.load("images/player1_L.png").convert()
        if self.rect.left > 0:
            # 針對物件本身移動 x(負的玩家移動速度), y 距離
            self.rect.move_ip(-TANK_SPEED, 0)
    def move_right(self):
        # 如果坦克還沒碰到右邊邊界
        self.image = pygame.image.load("images/player1_R.png").convert()
        if self.rect.right < WINDOW_WIDTH:
            # 針對物件本身移動 x(玩家移動速度), y 距離
            self.rect.move_ip(TANK_SPEED, 0)
    def move_up(self):
        # 如果坦克還沒碰到上面邊界
        self.image = pygame.image.load("images/player1_U.png").convert()
        if self.rect.top > 0:
            # 針對物件本身移動 x(玩家移動速度), y 距離
            self.rect.move_ip(0,-TANK_SPEED)
    def move_down(self):
        # 如果坦克還沒碰到下面邊界
        self.image = pygame.image.load("images/player1_D.png").convert()
        if self.rect.bottom < WINDOW_HEIGHT:
            # 針對物件本身移動 x(玩家移動速度), y 距離
            self.rect.move_ip(0,TANK_SPEED)
